    private void btn_ThongKeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ThongKeActionPerformed
        String selected = (String) comboTK.getSelectedItem();

        if (selected != null) {
            if (selected.equals("Hôm nay")) {
                dsHoaDon = hoaDonDAO.thongKeHomNay(dsHoaDon);
                loadTable(dsHoaDon);
            } else if (selected.equals("Hôm qua")) {
                dsHoaDon = hoaDonDAO.thongKeHomQua(dsHoaDon);
                loadTable(dsHoaDon);
            } else if (selected.equals("Tháng này")) {
                dsHoaDon = hoaDonDAO.thongKeThangNay(dsHoaDon);
                loadTable(dsHoaDon);
            } else if (selected.equals("Tháng trước")) {
                dsHoaDon = hoaDonDAO.thongKeThangTruoc(dsHoaDon);
                loadTable(dsHoaDon);
            } else if (selected.matches("\\d{4}")) { // Kiểm tra nếu là năm
                dsHoaDon = hoaDonDAO.thongKeTheoNam(dsHoaDon, Integer.parseInt(selected));
                loadTable(dsHoaDon);
            } else {
                loadDataToTable();
            }
        }
        
    }//GEN-LAST:event_btn_ThongKeActionPerformed

