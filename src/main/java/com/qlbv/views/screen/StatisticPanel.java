    private void loadTable(List<HoaDon> dsHoaDons){
        TableModel.setRowCount(0);
        
        for (HoaDon hd : dsHoaDons) {
            Object[] rowData = {
                hd.getMaHD(),
                khachHangDAO.timKhachHang(hd.getMaKH().getMaKH()).getTenKH(),
                nhanVienDAO.timNhanVien(hd.getMaNV().getMaNV()).getTenNV(),
                hd.getNgayTao(),
                df.format(hd.getTongTien())
            };
            TableModel.addRow(rowData);
        }
        table_thongke.revalidate();
        table_thongke.repaint();
        
        lb_tongTien.setText(String.format("%10s %20s", "Tổng tiền:",  df.format(hoaDonDAO.tinhDoanhThu(dsHoaDon))));
    }
    
