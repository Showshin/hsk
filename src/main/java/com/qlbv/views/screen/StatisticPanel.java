    private void timHoaDon(){
        String selectedItem = (String) combo_timKiem.getSelectedItem();
        String text_tim = txt_tim.getText().trim();
        HoaDon hd = null;
        
        if (selectedItem.trim().equals("Mã hóa đơn")) {
            hd = hoaDonDAO.timHoaDonTheoMaHD(text_tim, dsHoaDon);
        } else if (selectedItem.trim().equals("Mã khách hàng")) {
            hd = hoaDonDAO.timHoaDonTheoMaKH(text_tim, dsHoaDon);
        } else if (selectedItem.trim().equals("Mã nhân viên")) {
            hd = hoaDonDAO.timHoaDonTheoMaNV(text_tim, dsHoaDon);
        }
        if(hd == null){
            JOptionPane.showMessageDialog(this, "Không tìm thấy!");
            return;
        }
        TableModel.setRowCount(0);
        Object[] rowData = {
                hd.getMaHD(),
                khachHangDAO.timKhachHang(hd.getMaKH().getMaKH()).getTenKH(),
                nhanVienDAO.timNhanVien(hd.getMaNV().getMaNV()).getTenNV(),
                hd.getNgayTao(),
                df.format(hd.getTongTien())
            };
        TableModel.addRow(rowData);
        lb_tongTien.setText("");
    }
    
